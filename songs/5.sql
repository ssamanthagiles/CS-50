-- The average energy of all the songs.
SELECT
  AVG(energy)
FROM
  songs;

-- An aggregate function in SQL performs a calculation on a set of values and returns a single value.
-- In SQL, the FROM clause is used to specify the table that you want to select or delete data from.
-- So in the query, "songs" is the name of the table, not the database. The database would contain the "songs"
--table among potentially many others.